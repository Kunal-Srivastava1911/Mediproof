"""Render eval `Report` metrics to the markdown tables `make eval` writes into the README."""

from __future__ import annotations

import statistics
from pathlib import Path

from evals.harness import Report, coverage_accuracy_curve

_START = "<!-- BENCHMARK:START -->"
_END = "<!-- BENCHMARK:END -->"


def _pct(c: int, t: int) -> str:
    return f"{100 * c / t:.1f}%" if t else "—"


def render_markdown(report: Report) -> str:
    lines: list[str] = []
    lines.append(f"_Auto-generated by `make eval` over {report.n_claims} synthetic claims "
                 f"({report.n_clean} clean, {report.n_faulty} with a seeded fault). "
                 f"Fully reproducible from seeds._\n")

    # classification
    lines.append("**Document classification** (P0 keyword baseline)\n")
    lines.append(f"- accuracy: **{_pct(report.classification_correct, report.classification_total)}** "
                 f"({report.classification_correct}/{report.classification_total} documents)\n")

    # extraction
    lines.append("**Field extraction** (deterministic layer, vs ground truth)\n")
    lines.append("| Document type | Exact match | Normalized match |")
    lines.append("|---|---|---|")
    tot_e = [0, 0]
    tot_n = [0, 0]
    for t in sorted(report.exact_by_type):
        ec, et = report.exact_by_type[t]
        nc, nt = report.norm_by_type[t]
        tot_e[0] += ec; tot_e[1] += et; tot_n[0] += nc; tot_n[1] += nt
        lines.append(f"| {t} | {_pct(ec, et)} | {_pct(nc, nt)} |")
    lines.append(f"| **all** | **{_pct(*tot_e)}** | **{_pct(*tot_n)}** |\n")

    # audit
    lines.append("**Audit engine** (per fault type, vs injected ground truth)\n")
    lines.append("| Fault type | Recall |")
    lines.append("|---|---|")
    for fault in sorted(report.fault_recall):
        c, t = report.fault_recall[fault]
        lines.append(f"| {fault} | {_pct(c, t)} ({c}/{t}) |")
    fp_rate = report.clean_false_positives / report.n_clean if report.n_clean else 0.0
    lines.append("")
    lines.append(f"- **False-positive rate on clean files:** {fp_rate:.2f} severe review items "
                 f"per clean claim ({report.clean_false_positives} across {report.n_clean}).\n")

    # coverage-accuracy
    curve = coverage_accuracy_curve(report.coverage_points)
    if curve:
        lines.append("**Coverage–accuracy** (route the lowest-confidence fields to review)\n")
        lines.append("| Sent to review | Auto-accepted | Accuracy of auto-accepted |")
        lines.append("|---|---|---|")
        for reviewed, kept, acc in curve:
            lines.append(f"| {reviewed*100:.0f}% | {kept*100:.0f}% | {acc*100:.1f}% |")
        lines.append("")

    # hybrid unit economics
    sc = report.source_counts
    total_fields = sum(sc.values()) or 1
    rule = sc.get("rule", 0) + sc.get("fusion", 0)
    llm = sc.get("llm", 0)
    med = statistics.median(report.latencies) if report.latencies else 0.0
    lines.append("**Hybrid unit economics**\n")
    lines.append(f"- deterministic layer fills **{100*rule/total_fields:.1f}%** of extracted "
                 f"fields; **{llm} LLM calls** were needed across the whole set "
                 f"(the LLM only touches low-confidence fields).")
    lines.append(f"- median end-to-end latency: **{med*1000:.0f} ms/claim** (digital path, no OCR).\n")
    return "\n".join(lines)


def write_results(report: Report, path: str | Path) -> Path:
    path = Path(path)
    path.write_text("# MediProof benchmark results\n\n" + render_markdown(report),
                    encoding="utf-8")
    return path


def update_readme(report: Report, readme_path: str | Path) -> bool:
    """Replace the README block between the BENCHMARK markers. Returns True if it updated."""
    readme_path = Path(readme_path)
    text = readme_path.read_text(encoding="utf-8")
    block = f"{_START}\n\n{render_markdown(report)}\n\n{_END}"
    if _START in text and _END in text:
        pre = text[: text.index(_START)]
        post = text[text.index(_END) + len(_END):]
        readme_path.write_text(pre + block + post, encoding="utf-8")
        return True
    return False
